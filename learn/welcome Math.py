import math

print(math.ceil(4.3)) #Округление до большего целого числа

print(math.fabs(-4.3)) #Приведение к абсолютному значению числа

print(math.factorial(5)) #Вычисление факториала

print(math.floor(4.9)) #Округление до целого числ в меньшую сторону

print(math.pow(2,3)) #Возведение Х в степень У

print(math.sqrt(25)) #Корень из числа

